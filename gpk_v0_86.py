def gcdExtended(a, b):  
    # Base Case  
    if a == 0 :   
        return b, 0, 1
                 
    gcd, x1, y1 = gcdExtended(b%a, a)  
     
    # Update x and y using results of recursive  
    # call  
    x = y1 - (b//a) * x1  
    y = x1  
    return gcd, x, y 

# mod calculates (x**y%n)
def mod(x,y,n):
    p = 1
    s = x
    r = y
    while r > 0:
        if r % 2 == 1:
            p = p*s % n
        s = s*s % n 
        r = r // 2 
    return(p)

