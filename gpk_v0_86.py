def RSA_sys(n):
    print("Generating Public Keys and Private Keys")
    #finds p,q that are two prime numbers with many digits 
    p = prime_gen(n)
    q = prime_gen(n)
    #Generate Public_key_1:N
    N = p*q
    phi = (p-1)*(q-1)
    #Generate Public_key_2:e
    e = get_e(phi)
    #Generate Private_key:d
    d = get_mul_inv(e,phi)
    return N,e,d

